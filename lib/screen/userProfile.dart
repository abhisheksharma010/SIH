class UserProfile {
  final String firstName;
  final String lastName;
  final int age;
  final String dateOfBirth;
  final String city;
  final String country;
  final String email;
  final String number;

  UserProfile({
    required this.firstName,
    required this.lastName,
    required this.age,
    required this.dateOfBirth,
    required this.city,
    required this.country,
    required this.email,
    required this.number,
  });
}
